    class Caretaker implements ActionListener{
        Main m;
        Caretaker(Main m){
            this. m = m;
        }
        
        
        @Override
        public void actionPerformed(ActionEvent e) {
            if(e.getSource() == jButton1){
                m.pl.setVisible(true);
                m.p1.setVisible(false);
                m.p2.setVisible(false);
                m.ps.setVisible(false);
                jButton2.setVisible(true);
            }else
                if(e.getSource() == jButton2){
                    m.ps.fai.clear();
                    m.ps.setVisible(true);
                    m.pl.setVisible(false);
                    m.p1.setVisible(false);
                    m.p2.setVisible(false);
                    jButton3.setVisible(true);
                }else
                    if(e.getSource() == jButton3){
                        m.ps.setVisible(false);
                        m.pl.setVisible(false);
                        if(m.ps.getGType().equals("torta")){
                            m.p1.makeChart();
                            m.p1.setVisible(true);
                            m.p2.setVisible(false);
                        }else{
                            m.p2.makeChart();
                            m.p1.setVisible(false);
                            m.p2.setVisible(true);
                        }
                    }
                        
        }
    }
